from pathlib import Path
from tempfile import TemporaryDirectory
from contextlib import redirect_stdout
from io import StringIO
import json
import subprocess
import sys
import unittest
from warden_drydock import __version__
from warden_drydock import standalone
from warden_drydock.cli import main
from warden_drydock.core.generator import init_campaign
from warden_drydock.core.validation import validate_campaign
from warden_drydock.core.context import build_context
from warden_drydock.core.upgrade import upgrade_campaign
from warden_drydock.standalone import create_entity

class GeneratorTest(unittest.TestCase):
    def test_mothership_campaign_generation(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            self.assertTrue((root/'.drydock.json').exists())
            manifest=json.loads((root/'.drydock.json').read_text())
            self.assertEqual(manifest['framework_version'],__version__)
            adapter=json.loads((root/'00-drydock'/'adapter.json').read_text())
            self.assertEqual(manifest['adapter'],adapter['adapter'])
            self.assertEqual(manifest['adapter_version'],adapter['adapter_version'])
            lock=json.loads((root/'.drydock-lock.json').read_text())
            self.assertEqual(lock['schema_version'],1)
            self.assertEqual(lock['adapter_version'],adapter['adapter_version'])
            self.assertEqual(
                lock['files']['01-campaign/campaign-overview.md']['ownership'],
                'campaign',
            )
            self.assertEqual(
                lock['files']['00-drydock/ai-context.md']['ownership'],
                'generated',
            )
            self.assertEqual(lock['files']['templates/npc.md']['ownership'],'shared')
            self.assertIn('Test Campaign',(root/'README.md').read_text())
            self.assertTrue((root/'templates'/'npc.md').exists())
            self.assertEqual(
                (root/'scripts'/'drydock.py').read_text(encoding='utf-8'),
                Path(standalone.__file__).read_text(encoding='utf-8'),
            )
            self.assertEqual(validate_campaign(root),0)
            self.assertTrue(build_context(root).exists())

    def test_bootstrap_creates_context_and_validates_standalone(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            self.assertEqual(main(['bootstrap',str(root),'--name','Test Campaign']),0)
            context=(root/'00-drydock'/'ai-context.md').read_text(encoding='utf-8')
            self.assertIn('Generated by Warden Drydock',context)
            self.assertIn('Mothership System Principles',context)
            result=subprocess.run(
                [sys.executable,str(root/'scripts'/'drydock.py'),'validate'],
                cwd=root,
                capture_output=True,
                text=True,
                check=False,
            )
            self.assertEqual(result.returncode,0,result.stdout+result.stderr)

    def test_bootstrap_refuses_non_empty_directory(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            root.mkdir()
            existing=root/'notes.md'
            existing.write_text('user content',encoding='utf-8')
            with self.assertRaises(SystemExit):
                main(['bootstrap',str(root),'--name','Test Campaign'])
            self.assertEqual(existing.read_text(encoding='utf-8'),'user content')

    def test_context_uses_only_approved_sessions_and_is_stable(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            logs=root/'12-sessions'/'logs'
            logs.mkdir(parents=True,exist_ok=True)
            (logs/'001-approved.md').write_text(
                '---\nid: session-001\ntype: session\nstatus: canon\n---\n\n# Approved Event\n',
                encoding='utf-8',
            )
            (logs/'999-draft.md').write_text(
                '---\nid: session-999\ntype: session\nstatus: draft\n---\n\n# Unapproved Idea\n',
                encoding='utf-8',
            )
            output=build_context(root)
            first=output.read_text(encoding='utf-8')
            build_context(root)
            self.assertEqual(output.read_text(encoding='utf-8'),first)
            self.assertIn('Approved Event',first)
            self.assertNotIn('Unapproved Idea',first)

    def test_standalone_validator_detects_merge_conflicts(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            (root/'conflict.md').write_text('<<<<<<< ours\n=======\n>>>>>>> theirs\n')
            result=subprocess.run(
                [sys.executable,str(root/'scripts'/'drydock.py'),'validate'],
                cwd=root,
                capture_output=True,
                text=True,
                check=False,
            )
            self.assertEqual(result.returncode,1)
            self.assertIn('merge conflict marker',result.stdout)

    def test_upgrade_previews_then_restores_missing_managed_file(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            readme=root/'README.md'
            readme.unlink()
            preview=StringIO()
            with redirect_stdout(preview):
                self.assertEqual(upgrade_campaign(root),0)
            self.assertFalse(readme.exists())
            self.assertIn('Would update: README.md',preview.getvalue())
            self.assertEqual(upgrade_campaign(root,apply=True),0)
            self.assertTrue(readme.exists())

    def test_upgrade_blocks_modified_managed_file_and_preserves_campaign(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            readme=root/'README.md'
            readme.write_text('local customization',encoding='utf-8')
            campaign=root/'01-campaign'/'campaign-overview.md'
            original=campaign.read_text(encoding='utf-8')
            # Simulate a framework update by changing the recorded baseline.
            lock=json.loads((root/'.drydock-lock.json').read_text())
            lock['files']['README.md']['sha256']='obsolete-baseline'
            (root/'.drydock-lock.json').write_text(json.dumps(lock),encoding='utf-8')
            output=StringIO()
            with redirect_stdout(output):
                self.assertEqual(upgrade_campaign(root,apply=True),1)
            self.assertEqual(readme.read_text(encoding='utf-8'),'local customization')
            self.assertEqual(campaign.read_text(encoding='utf-8'),original)
            self.assertIn('CONFLICT: README.md',output.getvalue())

    def test_adapter_declares_and_creates_campaign_entities(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            entity=create_entity(root,'npc','npc-ripley','Ripley')
            text=entity.read_text(encoding='utf-8')
            self.assertEqual(entity.relative_to(root).as_posix(),'05-characters/npcs/npc-ripley.md')
            self.assertIn('id: npc-ripley',text)
            self.assertIn('ownership: campaign',text)
            self.assertIn('name: "Ripley"',text)
            self.assertIn('# Ripley',text)
            self.assertEqual(validate_campaign(root),0)
            with self.assertRaises(SystemExit):
                create_entity(root,'npc','npc-ripley','Duplicate')

    def test_semantic_validation_rejects_missing_adapter_fields(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            entity=create_entity(root,'faction','faction-company','The Company')
            entity.write_text(
                entity.read_text(encoding='utf-8').replace('status: idea\n',''),
                encoding='utf-8',
            )
            output=StringIO()
            with redirect_stdout(output):
                self.assertEqual(validate_campaign(root),1)
            self.assertIn('missing required field status',output.getvalue())

    def test_validation_rejects_inconsistent_adapter_metadata(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            adapter_path=root/'00-drydock'/'adapter.json'
            adapter=json.loads(adapter_path.read_text(encoding='utf-8'))
            adapter['adapter_version']='9.9.9'
            adapter_path.write_text(json.dumps(adapter),encoding='utf-8')
            output=StringIO()
            with redirect_stdout(output):
                self.assertEqual(validate_campaign(root),1)
            self.assertIn(
                'adapter_version does not match adapter.json', output.getvalue()
            )

    def test_adapter_visibility_rules_are_declarative(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            entity=create_entity(root,'npc','npc-leak','Leak')
            entity.write_text(
                entity.read_text(encoding='utf-8').replace(
                    'visibility: warden', 'visibility: players'
                ),
                encoding='utf-8',
            )
            output=StringIO()
            with redirect_stdout(output):
                self.assertEqual(validate_campaign(root),1)
            result=output.getvalue()
            self.assertIn('visibility must be warden for npc',result)
            self.assertIn(
                'forbidden field combination visibility=players, warden_only=true',
                result,
            )

    def test_adapter_validation_rejects_malformed_declarations(self):
        with TemporaryDirectory() as tmp:
            root=Path(tmp)/'campaign'
            init_campaign(root,name='Test Campaign',adapter='mothership')
            adapter_path=root/'00-drydock'/'adapter.json'
            adapter=json.loads(adapter_path.read_text(encoding='utf-8'))
            adapter['validation']['field_values']='not-an-object'
            adapter_path.write_text(json.dumps(adapter),encoding='utf-8')
            output=StringIO()
            with redirect_stdout(output):
                self.assertEqual(validate_campaign(root),1)
            self.assertIn(
                'validation.field_values must map fields', output.getvalue()
            )

if __name__=='__main__': unittest.main()
